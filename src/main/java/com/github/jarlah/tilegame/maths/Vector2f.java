package com.github.jarlah.tilegame.maths;

public class Vector2f {
	private float x, y;

	public Vector2f(float i, float j) {
		this.x = i;
		this.y = j;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
}
